    public void llenarTabla(){
    ArrayList<persona> lista = crt.Sala(1);
    
    DefaultTableModel modelo = new DefaultTableModel();
    this.tblComuna.setModel(modelo);
    modelo.addColumn("Rut");
    modelo.addColumn("Nombre");
    modelo.addColumn("n째 Urna");
    modelo.addColumn("n째 Sala");
    modelo.addColumn("Nombre Recinto");
    
    Object[] col = new Object[5];
    
    for(persona tmp : lista){
        col[0] = tmp.getRut();
        col[1] = tmp.getNombre_p(); 
        col[2] = tmp.getAppaterno(); 
        col[3] = tmp.getApmaterno();
        col[4] = tmp.getSexo(); 
        modelo.addRow(col);         
    }
    
}
